package hesapmakinesi;

public class hesapmakinesi {

	int sayi1;
	int sayi2;
	
	 hesapmakinesi() {
	}
	
	public int toplama (int sayi1 ,int sayi2 ) {
		
		 return  sayi1 + sayi2;
	}
	
	public int cikarma(int sayi1 ,int sayi2 ) {
		
		return sayi1 - sayi2;
		
	}
	public int carpma(int sayi1 ,int sayi2 ) {
		
		return sayi1 * sayi2;
	}
	
	public int bolme (int sayi1 ,int sayi2 ) {
	 
		return sayi1 / sayi2;
	}
	 
}
